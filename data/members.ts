// Helper functions
export function getMemberById(id: string): Member | undefined {
  return members.find((member) => member.id === id);
}

export function getMembersByIds(ids: string[]): Member[] {
  return members.filter((member) => ids.includes(member.id));
}

export function searchMembers(query: string): Member[] {
  const lowerQuery = query.toLowerCase();
  return members.filter(
    (member) =>
      member.name.toLowerCase().includes(lowerQuery) ||
      member.aspiration.toLowerCase().includes(lowerQuery) ||
      member.quote?.toLowerCase().includes(lowerQuery)
  );
}

export function getMembersCount(): number {
  return members.length;
}
